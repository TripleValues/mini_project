
-- `metro_line` 테이블 생성
CREATE TABLE `metro_line` (
	`역번호` VARCHAR(50) NOT NULL COLLATE 'utf8mb4_uca1400_ai_ci',
	`호선` VARCHAR(50) NOT NULL COLLATE 'utf8mb4_uca1400_ai_ci',
	`역명` VARCHAR(200) NULL DEFAULT NULL COLLATE 'utf8mb4_uca1400_ai_ci',
	`외부코드` VARCHAR(50) NULL DEFAULT NULL COLLATE 'utf8mb4_uca1400_ai_ci',
	PRIMARY KEY (`역번호`) USING BTREE,
	INDEX `idx_code` (`역번호`) USING BTREE
)
COLLATE='utf8mb4_unicode_ci'
ENGINE=InnoDB
;

